    <p style="text-align: justify;"> Trong những năm qua, được sự chỉ đạo của Bộ Tài chính, Ủy ban nhân dân các tỉnh, Thành phố hoạt động xổ số đã có nhiều bước phát triển với tốc độ tăng bình quân khoảng trên 20% năm. Thị trường xổ số mở rộng, hệ thống đại lý rộng khắp đã giải quyết được việc làm cho một lực lượng lớn lao động trong xã hội. Hoạt động kinh doanh xổ số đã có những đóng góp đáng kể vào ngân sách nhà nước, ngân sách các địa phương để đầu tư cho sự nghiệp y tế, giáo dục và các công trình phúc lợi xã hội khác. Theo số liệu thống kê, chỉ tính riêng năm 2008, tổng doanh thu xổ số của cả nước đạt 28.551 tỷ đồng, chiếm 1,9% GDP và thu nộp ngân sách nhà nước (đạt 8.028 tỷ đồng), bằng 3,5% tổng thu nội địa của các địa phương và 5,99% thu ngân sách của các địa phương.</p>
    <p style="text-align: justify;"> Bên cạnh những kết quả đạt được, hoạt động xổ số thời gian qua còn bộc lộ một số hạn chế, tồn tại lớn như quy mô thị trường xổ số, đặt cược và trò chơi có thưởng còn nhỏ. Hoạt động xổ số kiến thiết (XSKT) phát triển chưa thực sự ổn định, vững chắc, chưa khai thác hết thị trường tiềm năng, khả năng thu hút và phục vụ các đối tượng khách hàng được phép tham gia còn hạn chế. Các hình thức đặt cược chưa phong phú. Công tác nghiên cứu, ứng dụng các loại hình xổ số mới, gắn liền với các hoạt động kinh doanh dịch vụ đặt cược chưa được xác định và quan tâm. Bên cạnh đó, các dịch vụ ăn theo XSKT vẫn tồn tại ngày càng tinh vi hơn.</p>
