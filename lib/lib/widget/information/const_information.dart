    <p style="text-align: justify;"> Nghị định 78/2012/NĐ-CP đã khẳng định cụ thể nguyên tắc kinh doanh xổ số là loại hình kinh doanh có điều kiện, chịu sự kiểm soát chặt chẽ của cơ quan quản lý nhà nước để đảm bảo đáp ứng nhu cầu giải trí của một bộ phận người dân có thu nhập chính đáng và đảm bảo an toàn an ninh trật tự xã hội. Chỉ có doanh nghiệp (DN) được cơ quan nhà nước có thẩm quyền cấp giấy chứng nhận đủ điều kiện kinh doanh xổ số mới được phép tổ chức hoạt động kinh doanh xổ số. Đây là quy định rất mới và cụ thể hơn so với Nghị định 30/2007/NĐ-CP trước đây (chỉ quy định DN muốn kinh doanh phải được cơ quan nhà nước có thẩm quyền cấp giấy chứng nhận đủ điều kiện; và phải đảm bảo minh bạch, khách quan, trung thực, bảo vệ quyền và lợi ích của các bên tham gia).</p>
